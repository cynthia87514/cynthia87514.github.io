function show1()
{
    var Q1 = document.getElementById('Q1');
    var A1_1 = document.getElementById('A1_1');
    var A1_2 = document.getElementById('A1_2');
    var A1_3 = document.getElementById('A1_3');
    if (A1_1.style.display === "none") {
        A1_1.style.display = 'block';
        A1_2.style.display = 'block';
        A1_3.style.display = 'block';         
    }
    else {
        A1_1.style.display = "none";
        A1_2.style.display = 'none';
        A1_3.style.display = 'none';
    }
}
